package bank.dto;

public class accCanDTO {
	String accnum;
	String bal;
	String depacc;

	public String getBal() {
		return bal;
	}

	public void setBal(String bal) {
		this.bal = bal;
	}

	public String getDepacc() {
		return depacc;
	}

	public void setDepacc(String depacc) {
		this.depacc = depacc;
	}

	public String getAccnum() {
		return accnum;
	}

	public void setAccnum(String accnum) {
		this.accnum = accnum;
	}
}
